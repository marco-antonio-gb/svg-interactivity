<script>
import items from '@/mixins/items'
export default {
    name: 'svgContainer',
    data() {
        return {
            id_selected: '',
            desc_selected: '',  
        }
    },
    mounted() {
        this.setInitialClass("CHUCK")
    },
    
    methods: {
        selectStand(event) {
            const id = event.currentTarget.id;
            const path = event.currentTarget.querySelector("path")
            this.removeAllClass()
            this.addClass(path)
            this.setItem(id)
        },
        setItem(ID) {
             items.products.map(res => {
                if (res.id === ID) {
                    this.id_selected = res.id
                    this.desc_selected = res.description
                }
            })
        },
        addClass(path) {
            if (path.classList.contains("selected")) {
                path.classList.remove("selected");
            } else {
                path.classList.add("selected");
            }
        },
        removeAllClass() {
            var elems = document.querySelectorAll("path");
            [].forEach.call(elems, function (el) {
                el.classList.remove("selected");
            });
        },
        setInitialClass(ID) {
            var item = document.getElementById(ID).getElementsByTagName('path')[0];
            item.classList.add("selected")
            this.setItem(ID)
        }
    },
}
